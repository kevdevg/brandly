/**
 * TemplateField — A single element in the template schema.
 * 
 * Each field has an explicit `nature` that determines its behavior:
 * - `static`: Fixed decorative element, never editable by end-user.
 * - `brand-variable`: Auto-fills from DesignMD (logo, name, handles). No form field generated.
 * - `editable-slot`: Becomes a form input in production. The only type that generates a form field.
 */
export interface TemplateField {
  id: string;
  nature: TemplateFieldNature;
  label: string;
  type: TemplateFieldType;
  required: boolean;

  /** Static content (for nature='static', the actual rendered content; for others, placeholder text) */
  content: string;

  /** Position in the canvas (% of canvas) */
  position: { x: number; y: number; w: number; h: number; rotation?: number };

  /** Brand auto-fill source (only for nature='brand-variable') */
  brandSource?: BrandSource;
  /** ID of a BrandContentPiece to use as default (e.g. a logo badge asset) */
  brandAssetId?: string;

  /** Validation rules (primarily for nature='editable-slot') */
  rules?: TemplateFieldRules;

  /** Visual style */
  style: TemplateFieldStyle;

  /** Order in the production form (only for editable-slots). Lower = first */
  formOrder: number;

  /** Layer visibility — hidden layers don't render on canvas but aren't deleted. Default: true */
  visible?: boolean;
  /** Layer lock — locked layers can't be selected/moved on canvas. Default: false */
  locked?: boolean;
  /** Background layer — pinned to the bottom of the z-stack. Default: false */
  isBackground?: boolean;
}

// ═══ Legacy Express Field (kept for backward compatibility) ═══

/** A single editable field within a scene (legacy format) */
export interface ExpressField {
  id: string;
  type: 'text' | 'media' | 'logo' | 'shape';
  label: string;
  placeholder: string;
  required: boolean;
  /**
   * Brand variable source — auto-resolves from DesignMD / CompanyProfile.
   * e.g. 'brand-name' → company.name, 'instagram' → company.socialLinks.instagram
   * When set, the field auto-fills but the user can still override it.
   */
  brandSource?: BrandSource;
  /** ID of a BrandContentPiece to use as default value (e.g. a logo badge asset) */
  brandAssetId?: string;
  /** Position within the scene (% of scene canvas) */
  position: { x: number; y: number; w: number; h: number };
  /** Default style */
  style: {
    fontSize?: number;
    fontWeight?: number;
    fontFamily?: string;
    textAlign?: string;
    color?: string;       // If not set, resolved from brandSource
    opacity?: number;
    // Shape-specific
    shapeType?: 'rectangle' | 'circle' | 'triangle' | 'line' | 'arrow' | 'star';
    shapeFill?: string;
    shapeStroke?: string;
    shapeStrokeWidth?: number;
    shapeCornerRadius?: number;
  };
}

/** A single scene block in the storyboard */
export interface ExpressScene {
  id: string;
  type: 'intro' | 'content' | 'outro' | 'transition';
  name: string;
  durationSeconds: number;
  layout: SceneLayout;
  /** Legacy field format (backward compat) */
  editableFields: ExpressField[];
  /** New schema-based fields (used by redesigned builder) */
  fields?: TemplateField[];
  /** Scene-level transition animation */
  transition?: { type: string; duration: number };
  /** Background: solid color, gradient, or media (user-replaceable) */
  background?: {
    type: 'solid' | 'gradient' | 'media' | 'brand';
    value?: string;   // color hex or media URL
  };
  /** Thumbnail for storyboard card display */
  thumbnailUrl?: string;
  /** Source for intro/outro scenes: 'brand' = auto from DesignMD, 'form' = user uploads video */
  segmentSource?: 'brand' | 'form';
  /** Form field label (only when segmentSource === 'form'), e.g. "Video de intro" */
  segmentFieldLabel?: string;
  /** Whether the form field is required (only when segmentSource === 'form'). Default true */
  segmentFieldRequired?: boolean;
  /** Transition applied at the boundary of this segment (e.g. intro→content or content→outro) */
  segmentTransition?: { type: string; duration: number };
  /** Video position for segment scenes (center-based %, like TemplateField.position) */
  segmentVideoX?: number;      // center X (0-100), default 50
  segmentVideoY?: number;      // center Y (0-100), default 50
  segmentVideoW?: number;      // width (0-100), default 100
  segmentVideoH?: number;      // height (0-100), default 100
  /** Object fit mode for the segment video */
  segmentVideoFit?: 'cover' | 'contain' | 'fill';
}

/** Express template — a sequence of scenes */
export interface ExpressTemplate {
  id: string;
  name: string;
  description: string;
  category: 'social' | 'ad' | 'promo' | 'story' | 'announcement';
  icon: string;
  aspectRatio: '9:16' | '16:9' | '1:1' | '4:5' | '4:3';
  format: 'video' | 'image';
  /** Ordered list of scenes (the storyboard) */
  scenes: ExpressScene[];
  /** If set, this template belongs to a specific brand */
  brandId?: string;
  /** Auto-include brand audio */
  usesBrandAudio: boolean;
  /** User-created template */
  isCustom?: boolean;
  createdAt?: string;
}
