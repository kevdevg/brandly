  /** Show the platform icon (default: true) */
  showIcon: boolean;
  /** Icon placement relative to text (default: 'left') */
  iconPosition: 'left' | 'right';
  /** Show @ before handle (default: true for socials, false for website) */
  showAtPrefix: boolean;
